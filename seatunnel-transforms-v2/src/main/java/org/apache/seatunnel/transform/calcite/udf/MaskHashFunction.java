package org.apache.seatunnel.transform.calcite.udf;

import com.google.auto.service.AutoService;

import java.nio.charset.StandardCharsets;
import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;

/**
 * SHA-256 hash UDF for data masking scenarios. Produces a deterministic hash of the original value
 * for downstream comparison / JOIN / GROUP BY.
 *
 * <p>Usage: {@code MASK_HASH(value)}
 */
@AutoService(CalciteUdf.class)
public class MaskHashFunction implements CalciteUdf {

    @Override
    public String functionName() {
        return "MASK_HASH";
    }

    public static String eval(String value) {
        if (value == null) {
            return null;
        }
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] hash = digest.digest(value.getBytes(StandardCharsets.UTF_8));
            StringBuilder hexString = new StringBuilder(hash.length * 2);
            for (byte b : hash) {
                String hex = Integer.toHexString(0xff & b);
                if (hex.length() == 1) {
                    hexString.append('0');
                }
                hexString.append(hex);
            }
            return hexString.toString();
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException("SHA-256 algorithm not available", e);
        }
    }
}
